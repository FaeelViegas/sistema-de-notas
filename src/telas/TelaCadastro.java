    private void btnCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraActionPerformed
        Alunos objAluno = new Alunos();
        if (txtNome.getText().isEmpty() || txtNota1.getText().isEmpty() || txtNota2.getText().isEmpty()|| txtDisciplina.getText().isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "PREENCHA TODOS OS CAMPOS!");
            return;
        }
        try {
            String nome = txtNome.getText();
            String disciplina = txtDisciplina.getText();
            float nota1 = Float.parseFloat(txtNota1.getText());
            float nota2 = Float.parseFloat(txtNota2.getText());
            float media = (nota1 + nota2) / 2;
            boolean aprovado = false;
            if (media >= 7) {
                aprovado = true;
            }

            objAluno.setNomeCompleto(nome);
            objAluno.setNota1(nota1);
            objAluno.setNota2(nota2);
            objAluno.setMedia(media);
            objAluno.setAprovado(aprovado);
            objAluno.setDisciplina(disciplina);

            AlunosDAO alunosDao = new AlunosDAO();
            alunosDao.cadastrarAluno(objAluno);
            limparCampos();
        } catch (NumberFormatException e) {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "OS VALORES DO CAMPO NOTA S√ÉO INVALIDOS!");
        }

    }//GEN-LAST:event_btnCadastraActionPerformed

    public void limparCampos() {
        txtNome.setText("");
        txtNota1.setText("");
        txtNota2.setText("");
        txtDisciplina.setText("");
    }

